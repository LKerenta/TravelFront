package com.travel.front.Entity;

public class Franchise {
    private Integer FranID;
    private String Password;
    private String FranName;
    private String FranImage;
    private String WhoInCharge;
    private String ChargeID;
    private Integer ChargePhone;
    private String Phone;
    private String Email;
    private Integer Balance;
    private Integer CreditCard;
    private String Introduce;

    public String getIntroduce() {
        return Introduce;
    }

    public void setIntroduce(String introduce) {
        Introduce = introduce;
    }

    public Integer getFranID() {
        return FranID;
    }

    public void setFranID(Integer franID) {
        FranID = franID;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String password) {
        Password = password;
    }

    public String getFranName() {
        return FranName;
    }

    public void setFranName(String franName) {
        FranName = franName;
    }

    public String getFranImage() {
        return FranImage;
    }

    public void setFranImage(String franImage) {
        FranImage = franImage;
    }

    public String getWhoInCharge() {
        return WhoInCharge;
    }

    public void setWhoInCharge(String whoInCharge) {
        WhoInCharge = whoInCharge;
    }

    public String getChargeID() {
        return ChargeID;
    }

    public void setChargeID(String chargeID) {
        ChargeID = chargeID;
    }

    public Integer getChargePhone() {
        return ChargePhone;
    }

    public void setChargePhone(Integer chargePhone) {
        ChargePhone = chargePhone;
    }

    public String getPhone() {
        return Phone;
    }

    public void setPhone(String phone) {
        Phone = phone;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public Integer getBalance() {
        return Balance;
    }

    public void setBalance(Integer balance) {
        Balance = balance;
    }

    public Integer getCreditCard() {
        return CreditCard;
    }

    public void setCreditCard(Integer creditCard) {
        CreditCard = creditCard;
    }
}
